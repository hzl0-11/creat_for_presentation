function preSpeedSubnowSpeed_a()
        -- 初始设为0
    local preSpeed = 0
    local nowSpeed = 0
    local subSpeed = 0 
    local PreSpeed = function ()        
            return string.format("%.2f",preSpeed)
    end  
    local NowSpeed = function (role)        
            nowSpeed = player.velMod(role)
            subSpeed = nowSpeed - preSpeed
            preSpeed = player.velMod(role)
            return string.format("%.2f",nowSpeed)
    end
    local SubSpeed = function ()       
        return string.format("%.2f",subSpeed)
    end
    return PreSpeed,NowSpeed,SubSpeed
end

